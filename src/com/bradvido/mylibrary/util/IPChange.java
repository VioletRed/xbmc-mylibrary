
package com.bradvido.mylibrary.util;

public class IPChange
{

    private String from, to;
    public IPChange(String from, String to)
    {
        this.from = from;
        this.to = to;
    }
    public String getFrom()
    {
        return from;
    }
    public String getTo()
    {
        return to;
    }
}
